#include <iostream>
using namespace std;
int main()
{
    int i, j;
    for (i = 1; i <= 18; i++)
    {
        for (j = 1; j <= i; j++)
        {
            if (i >= 10 && i / 2 < j)
            {
                if (i % 2 != 0)
                    cout << i % 10;
                break;
            }
            cout << i;
        }
        cout << endl;
    }
    return 0;
}